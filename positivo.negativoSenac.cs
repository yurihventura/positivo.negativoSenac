using System;

class Program
{
    static void Main()
    {
        int num;
        Console.WriteLine("Digite um número");
        num = int.Parse(Console.ReadLine());
        if (num >= 0)
        {
            Console.WriteLine("Número Positivo!");
        }
        else
        {
            Console.WriteLine("Número Negativo!");





        }
    }
}

